"""Сортировка Помещений внутри Здания.

Порядок: числовая часть этажа по убыванию (верхние этажи выше), затем
буквенная часть этажа А→Я, затем название А→Я. Помещения без числового
этажа уходят в конец списка. Этаж — строка вида «5», «1А», «-1P»,
«-1 Паркинг»; всегда начинается с (возможно отрицательной) цифры.
"""
import re

_FLOOR_RE = re.compile(r"^(-?\d+)(.*)$")


def room_sort_key(room):
    floor = (room.floor or "").strip()
    m = _FLOOR_RE.match(floor)
    if m:
        return (0, -int(m.group(1)), m.group(2).strip().lower(), (room.name or "").lower())
    # Без числового этажа — в конец, между собой по названию.
    return (1, 0, floor.lower(), (room.name or "").lower())
