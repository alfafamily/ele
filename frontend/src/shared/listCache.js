// In-memory кэш состояния списков, чтобы при возврате «назад» со страницы
// объекта список открывался ровно там, где его оставили: те же фильтры,
// сортировка и поиск, уже подгруженные страницы и позиция прокрутки.
//
// Живёт в памяти SPA — сбрасывается при полной перезагрузке страницы
// (тогда список честно грузится заново), но переживает клиентские переходы
// (в т.ч. кнопку «Назад»), где как раз и важно восстановление.
const store = new Map()

export function readListCache(key) {
  return store.get(key)
}

export function writeListCache(key, patch) {
  store.set(key, { ...store.get(key), ...patch })
}
